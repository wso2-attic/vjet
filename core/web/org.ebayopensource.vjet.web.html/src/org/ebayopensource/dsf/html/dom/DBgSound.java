	/**
	 * Returns how the background sound is divided between the left and right 
	 * speakers.  IE range is -10000 to +10000.  -10000 indicates all sound 
	 * directed to * the left speaker.  +10000 indicates all sound directed 
	 * to the right speaker.  0 indicates sound is balanced between right and 
	 * left speakers.
	 */
	public String getHtmlBalance() { 
		return getHtmlAttribute(EHtmlAttr.balance) ;
	}
